#!/usr/bin/env node
// Assembles index.html from template.html + sections/*.html.
//
// Edit the files in sections/ (or template.html for the page shell), then run:
//   node build.mjs
//
// The GitHub Actions deploy workflow runs this automatically before publishing,
// so pushing a change to a section file is enough — index.html is regenerated
// in CI. index.html is a build output: don't edit it by hand.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const dir = dirname(fileURLToPath(import.meta.url));
const NOTICE = '<!-- AUTO-GENERATED by build.mjs from template.html + sections/*.html. Do not edit this file directly. -->';

const template = readFileSync(join(dir, 'template.html'), 'utf8');

let count = 0;
const assembled = template.replace(
  /^[ \t]*<!--\s*@include\s+([\w.-]+)\s*-->[ \t]*$/gm,
  (_match, name) => {
    count++;
    return readFileSync(join(dir, 'sections', `${name}.html`), 'utf8').replace(/\s+$/, '');
  }
);

const out = assembled
  .replace('<!DOCTYPE html>\n', `<!DOCTYPE html>\n${NOTICE}\n`)
  .replace(/\n{3,}/g, '\n\n')
  .replace(/\s*$/, '') + '\n';

writeFileSync(join(dir, 'index.html'), out);
console.log(`Built index.html from ${count} section${count === 1 ? '' : 's'}.`);
